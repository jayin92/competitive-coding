int get_median(vector<vector<int>> a, pii pos, int k){
    int arr[k*k];
    bool flag = false;
    for(int i=pos.X;i<pos.X+k;i++){
        for(int j=pos.Y;j<pos.Y+k;j++){
            arr[(i-pos.X)*k+j-pos.Y] = a[i][j];
            if(a[i][j] <= ans) flag = true;
        }
    }

    if(!flag) return iNF; 

    return kthSmallest(arr, 0, k*k-1, k*k - (k*k/2+1)  + 1);
}


